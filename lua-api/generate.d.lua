---This function provides an interface to the internal routine used to convert trigger and alias "simple" wildcard expressions to full regular expressions.
---
---Example:
---
---```lua
---print(MakeRegularExpression("* goes *"))
---```
---
---Output: `"^(.*?) goes (.*?)$"`
---
---The behaviour is basically:
---
---* Put ^ at the start of the expression
---* Put $ at the end of the expression
---* Convert each occurrence of "*" to "(.*?)"
---* Non-printable characters in the range 0x00 to 0x1F become hex sequences
---* Non alphanumeric characters are escaped with a backslash.
---@param text string
---@return string regex The original string converted to a regular expression.
